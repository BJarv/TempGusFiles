package ca.runner.items;

import ca.runner.level.Level;

public abstract class HealthPotion extends Item{

	protected String name;
	protected int flatHealth;
	protected int healthOverTime;
	
	public HealthPotion(Level level, String name, int x, int y, int flatHealth, int healthOverTime) {
		super(level);
		this.name = name;
		this.x = x;
		this.y = y;
		this.flatHealth = flatHealth;
		this.healthOverTime = healthOverTime;
	}

	
	public String getName() {
		return name;
	}
	
}